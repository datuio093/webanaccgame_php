    /**
     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        $data = $request->validate(
            [
            'sodu' => 'bail|required|numeric|gt:0',
 
            'role'=>'required',


            ],

            [
               
                'sodu.required' => 'Số Dư không được để trống',
     
                'role.required' => 'Role    không được để trống',
                
            ]
        );
      
        $user =  User::find($id); 
 
        $user->money  = $data['sodu'];

        $user->role = $data['role'];

        $user->save();
        return redirect()->back();
    }

    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        $user = User::find($id);
   

   
        $user->integer('id')->unsigned();
        $user->foreign('user_id')->references('id')->on('users')->onDelete('cascade');
        return redirect()->back();

    }
